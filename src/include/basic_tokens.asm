******************************************************************************
* Definitions of some relevant tokens from TI BASIC.
******************************************************************************

token_line_terminator equ >00
token_open            equ >9f
token_colon           equ >b5
token_quoted_string   equ >c7
token_unquoted_string equ >c8
token_hash            equ >fd
